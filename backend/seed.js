function generateSlug(cleanTitle) {
  return cleanTitle
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '')
    .trim()
    .replace(/\s+/g, '-');
}

async function seed() {
  try {
    await mongoose.connect(process.env.MONGODB_URI);
    console.log('Connected to MongoDB');

    await Movie.deleteMany({});
    await Admin.deleteMany({});
    await SiteLink.deleteMany({});

    console.log('Cleared existing data');

    const moviesWithSlugs = SAMPLE_MOVIES.map((movie, index) => ({
      ...movie,
      slug: generateSlug(movie.cleanTitle) + (index > 0 ? `-${index}` : ''),
    }));

    const createdMovies = await Movie.insertMany(moviesWithSlugs);
    console.log(`Created ${createdMovies.length} movies`);

    const admin = new Admin({
      username: 'admin',
      email: 'admin@vegamovies.com',
      passwordHash: 'admin123',
      role: 'admin',
    });
    await admin.save();
    console.log('Created admin user (username: admin, password: admin123)');

    await SiteLink.insertMany([...SITE_LINKS_ROW_1, ...SITE_LINKS_ROW_2]);
    console.log('Created site links');

    console.log('Seeding complete!');
    process.exit(0);
  } catch (error) {
    console.error('Seeding failed:', error.message);
    process.exit(1);
  }
}

seed();
